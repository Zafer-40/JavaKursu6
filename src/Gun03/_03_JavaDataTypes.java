package Gun03;

public class _03_JavaDataTypes {
    public static void main(String[] args) {
        int sayi = 66; //hafizada int kadar yer kaplar , sadece tam sayi atilabilir.

        double kusurluSayi = 3.12345; // ondalikli sayilar icin kullanilir.

        char harf = 'A'; // tek bir harf yazmak icin kullanilir.
        String isim = "Zafer"; // isim kelime yazmak icin kullanilir.

        System.out.println("isim = " + isim);
        System.out.println("harf = " + harf);
        System.out.println("sayi = " + sayi);
        System.out.println("kusurluSayi = " + kusurluSayi);

    }
}
