package Gun08;

public class _06_JavaAssignmentOperators {
    public static void main(String[] args) {

        int a = 5;

        a= a+6; // a ya 6 degerini ekle 11 oldu
        a +=6;  // a ya 6 degerini ekle 17 oldu

        a=a+1;  // a yi 1 artir
        a++;    // a yi 1 artir
        ++a;    // a yi 1 artir
        a+=1;   // a yi 1 artir

        a= a-1; // a yi 1 azalt
        a--;    // a yi 1 azalt
        --a;    // a yi 1 azalt
        a-=1;   // a yi 1 azalt
        a-=25;  // a yi 25 azalt a = a - 25 demek

        /*******************************************/

        int b = 10;
        b = b/5;    // b yi 5 e bol
        b /= 5;     // b yi 5 e bol

        /*******************************************/

        int c = 10;
        c = c*5; //c yi 5 ile carp
        c *= 5;  //c yi 5 ile carp





    }
}
